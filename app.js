    async toggleRecommend(postId) {
        const recommended = this.getRecommendedPosts();
        const btn = document.getElementById('recommendBtn');
        const countEl = document.getElementById('recommendCount');
        const doc = await this.db.collection('posts').doc(postId).get();
        const current = doc.data()?.recommends || 0;

        if (recommended.includes(postId)) {
            recommended.splice(recommended.indexOf(postId), 1);
            await this.db.collection('posts').doc(postId).update({ recommends: Math.max(0, current-1) });
            btn?.classList.remove('recommended');
            if (countEl) countEl.textContent = Math.max(0, current-1);
            Animations.showToast('추천을 취소했습니다', 'info');
        } else {
            recommended.push(postId);
            await this.db.collection('posts').doc(postId).update({ recommends: current+1 });
            btn?.classList.add('recommended');
            if (countEl) countEl.textContent = current+1;
            Animations.showToast('추천했습니다! 👍', 'success');
            if (current+1 >= 5) Animations.showToast('🔥 인기글로 등록되었습니다!', 'success');
        }
        localStorage.setItem('sportsLive_recommendedPosts', JSON.stringify(recommended));
    },

    getRecommendedPosts() {
        try { return JSON.parse(localStorage.getItem('sportsLive_recommendedPosts') || '[]'); } catch { return []; }
    },

