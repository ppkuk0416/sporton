    loadPosts() {
        if (!this.db) return;
        const container = document.getElementById('postList');
        const hotContainer = document.getElementById('hotList');
        const hotSection = document.getElementById('hotSection');
        Animations.showSkeleton(container, 4);

        if (this.unsubscribePosts) this.unsubscribePosts();

        let query = this.db.collection('posts').orderBy('createdAt', 'desc').limit(50);
        if (this.currentCategory !== '전체') query = query.where('category', '==', this.currentCategory);

        this.unsubscribePosts = query.onSnapshot(snapshot => {
            container.innerHTML = '';
            hotContainer.innerHTML = '';

            if (snapshot.empty) {
                hotSection.style.display = 'none';
                container.innerHTML = '<p class="empty-state">📝 아직 게시글이 없어요. 첫 글을 작성해보세요!</p>';
                return;
            }

            const posts = [];
            snapshot.forEach(doc => posts.push({ id: doc.id, ...doc.data() }));

            // 인기글: 추천 5개 이상, 추천 순 정렬
            const hotPosts = posts.filter(p => (p.recommends || 0) >= 5)
                                  .sort((a,b) => (b.recommends||0) - (a.recommends||0));

            if (hotPosts.length > 0) {
                hotSection.style.display = 'block';
                hotPosts.forEach(post => hotContainer.appendChild(this.createPostItem(post, true)));
            } else {
                hotSection.style.display = 'none';
            }

            // 일반 목록
            posts.forEach(post => container.appendChild(this.createPostItem(post, false)));
        }, err => {
            console.error(err);
            container.innerHTML = '<p class="empty-state">게시글을 불러올 수 없습니다</p>';
        });
    },

