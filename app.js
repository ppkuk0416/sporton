    async getMatchesBySport(sportKey, statusFilter) {
        const cacheKey = `matches_${sportKey}_${statusFilter}`;
        const cached = DataCache.get(cacheKey);
        if (cached) return cached;

        const results = {};
        const configs = sportKey==='all'
            ? Object.entries(this.sportConfig).map(([k,v])=>({ key:k, config:v, leaguesSlice:2 }))
            : this.sportConfig[sportKey]
                ? [{ key:sportKey, config:this.sportConfig[sportKey], leaguesSlice:999 }]
                : [];

        for (const { key, config, leaguesSlice } of configs) {
            results[key] = { name:config.name, icon:config.icon, leagues:[] };
            const leagues = config.leagues.slice(0, leaguesSlice);
            await Promise.allSettled(leagues.map(async lg => {
                try {
                    let matches = await this._fetchLeague(lg, statusFilter);

                    // ── 상태 필터 ──
                    if (statusFilter==='live')     matches = matches.filter(m=>m.status==='live');
                    if (statusFilter==='upcoming') matches = matches.filter(m=>m.status==='upcoming');

                    // ── 정렬: live → upcoming → finished ──
                    const order = { live:0, upcoming:1, finished:2 };
                    matches.sort((a,b) => (order[a.status]??1) - (order[b.status]??1));

                    results[key].leagues.push({ ...lg, matches });
                } catch(e) {
                    console.warn(lg.name, '실패:', e.message);
                    results[key].leagues.push({ ...lg, matches:[] });
                }
            }));

            // 리그도 live 경기 있는 게 앞으로
            results[key].leagues.sort((a,b) => {
                const aHasLive = a.matches?.some(m=>m.status==='live') ? 0 : 1;
                const bHasLive = b.matches?.some(m=>m.status==='live') ? 0 : 1;
                return aHasLive - bHasLive;
            });
        }

        DataCache.set(cacheKey, results, 60);
        return results;
    },

    async _fetchLeague(lg, statusFilter) {
        if (statusFilter === 'upcoming') {
            // 오늘 + 앞으로 3일치 데이터 병렬 조회 (미래 예정 경기 표시)
            const dates = [];
            const now = new Date();
            for (let i = 0; i <= 3; i++) {
                const d = new Date(now);
                d.setDate(now.getDate() + i);
                dates.push(d.toISOString().slice(0,10).replace(/-/g,''));
            }
            const results = await Promise.allSettled(dates.map(ds => this.fetchESPN(lg.espnId, ds)));
            const all = [], seen = new Set();
            for (const r of results) {
                if (r.status === 'fulfilled') {
                    for (const m of r.value) {
                        if (!seen.has(m.id)) { seen.add(m.id); all.push(m); }
                    }
                }
            }
            return all;
        }
        return await this.fetchESPN(lg.espnId);
    },

